      SUBROUTINE PUTFAST(Z,IFIRST,NDIS,ISPIN,ITYPE)
C
C THIS ROUTINE DOES A FAST IN-CORE PUT OF A LIST HELD IN
C  THE AUXILIARY CACHE
C
CEND
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION Z(1)
      COMMON / / ICORE(1)
      COMMON /LISTS/ MOIO(10,500),MOIOWD(10,500),MOIOSZ(10,500),
     &               MOIODS(10,500),MOIOFL(10,500)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /FILES/ LUOUT,MOINTS
      COMMON /INCORE/ ICREC(2),USEC,IXT(2),IMOD(2)
      COMMON /IOSTAT/ NOPBUF,NOPDSK
      COMMON /FILSPC/ ILNBUF,IPRCLN,IPRCWD
      COMMON /IOPOS/ ICRSIZ,ICHCSZ,IOFF(2),LENREC
      COMMON /FLAGS/ IFLAGS(100)
      COMMON /AUXCACHE/ QUIKGET(10,500)
C
      DISSIZ=MOIOSZ(ISPIN,ITYPE)
      IPOS=QUIKGET(ISPIN,ITYPE)+(IFIRST-1)*DISSIZ*IINTFP
c YAU : old
c     LENGTH=NDIS*DISSIZ*IINTFP
c     CALL ICOPY(LENGTH,Z,1,ICORE(IPOS),1)
c YAU : new
      CALL DCOPY(NDIS*DISSIZ,Z,1,ICORE(IPOS),1)
c YAU : end
      RETURN
      END
