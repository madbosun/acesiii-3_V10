
/*
 * This routine returns the f_int value defined by a character string.
 */

#include <stdlib.h>

#include "f_types.h"

f_int c_atol (const char * str)
{ return (f_int)atol(str); }

f_int c_atol_(const char * str)
{ return (f_int)atol(str); }

f_int C_ATOL (const char * str)
{ return (f_int)atol(str); }

